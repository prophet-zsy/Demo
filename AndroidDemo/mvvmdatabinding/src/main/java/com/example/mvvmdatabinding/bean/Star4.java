package com.example.mvvmdatabinding.bean;

public class Star4 {
    private String name;
    private int fanNum;

    public Star4() {
    }

    public Star4(String name, int fanNum) {
        this.name = name;
        this.fanNum = fanNum;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getFanNum() {
        return fanNum;
    }

    public void setFanNum(int fanNum) {
        this.fanNum = fanNum;
    }
}
